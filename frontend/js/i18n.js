export let currentLang = localStorage.getItem('drivecheck_lang') || 'hu';

export function t(key) {
    if (!translations[currentLang]) return key;
    return translations[currentLang][key] || translations['hu'][key] || key;
}

export function translateDynamic(text) {
    if (!text) return text;
    let tr = String(text);

    if (currentLang === 'hu') return tr;

    const upperTr = tr.trim().toUpperCase();

    const actions = {
        'IGÉNYLÉS': 'act_request', 'BÍRÁLAT': 'act_review', 'LEZÁRÁS': 'act_close',
        'HOZZÁADÁS': 'act_add', 'MÓDOSÍTÁS': 'act_modify', 'TÖRLÉS': 'act_delete'
    };
    if (actions[upperTr]) return t(actions[upperTr]);

    if (tr.includes('Hibás adatok!')) tr = tr.replace('Hibás adatok!', t('err_invalid_credentials'));
    if (tr.includes('Új fuvarigény:')) tr = tr.replace('Új fuvarigény:', t('audit_new_trip'));
    if (tr.includes('állapota:')) {
        tr = tr.replace('Fuvar', t('audit_trip_word')).replace('állapota:', t('audit_status_word'));
        tr = tr.replace('JOVAHAGYOTT', t('status_approved')).replace('ELUTASITOTT', t('status_rejected'));
    }
    if (tr.includes('lezárva. Új km óra:')) {
        tr = tr.replace('Fuvar', t('audit_trip_word')).replace('lezárva. Új km óra:', t('audit_closed_km'));
    }
    if (tr.includes('KRITIKUS: A(z)')) {
        tr = tr.replace('KRITIKUS: A(z)', t('alert_critical')).replace('letiltva! Lejárt:', t('alert_disabled'));
    }

    return tr;
}

export function applyTranslations() {
    if (translations[currentLang] && translations[currentLang]['page_title']) {
        document.title = translations[currentLang]['page_title'];
    }

    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang] && translations[currentLang][key]) {
            if ((el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') && el.hasAttribute('placeholder') && el.type !== 'date') {
                el.placeholder = translations[currentLang][key];
            } else {
                el.innerHTML = translations[currentLang][key];
            }
        }
    });

    const filterInput = document.getElementById('kliensElozmenyekSzuro');
    if (filterInput && translations[currentLang]['filter_title']) {
        filterInput.title = translations[currentLang]['filter_title'];
    }
}

export function initLanguageSwitcher() {
    const langSelect = document.getElementById('langSelect');
    if (langSelect) {
        langSelect.value = currentLang;
        langSelect.addEventListener('change', function (e) {
            currentLang = e.target.value;
            localStorage.setItem('drivecheck_lang', currentLang);
            applyTranslations();
            if (typeof window.reRenderDynamicLanguage === 'function') {
                window.reRenderDynamicLanguage();
            }
        });
    }
    applyTranslations();

    const loginErrObserver = new MutationObserver(() => {
        const el = document.getElementById('loginError');
        if (el && el.innerText === 'Hibás adatok!' && currentLang !== 'hu') {
            el.innerText = t('err_invalid_credentials');
        }
    });
    const errNode = document.getElementById('loginError');
    if (errNode) loginErrObserver.observe(errNode, { childList: true, characterData: true, subtree: true });
}